# –°–æ–∑–¥–∞–µ–º —Ä–æ—É—Ç–µ—Ä –¥–ª—è –æ–±—Ä–∞–±–æ—Ç—á–∏–∫–æ–≤ –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª–∏
router = Router()


@router.message(Command("admin"))
async def admin_panel(message: types.Message):
    """
    –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç –∞–¥–º–∏–Ω-–ø–∞–Ω–µ–ª—å (—Ç–æ–ª—å–∫–æ –¥–ª—è –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞)
    """
    user = message.from_user
    
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∞–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
    if user.id != config.ADMIN_ID:
        await message.answer(
            text=get_text('ru', 'not_admin'),
            parse_mode="HTML"
        )
        return
    
    # –ü–æ–ª—É—á–∞–µ–º —è–∑—ã–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    user_lang = db.get_user_language(user.id)
    if not user_lang:
        user_lang = 'ru'
    
    await message.answer(
        text=get_text(user_lang, 'admin_panel'),
        reply_markup=get_admin_keyboard(user_lang),
        parse_mode="HTML"
    )


@router.callback_query(F.data == "admin_stats")
async def show_statistics(callback: types.CallbackQuery):
    """
    –ü–æ–∫–∞–∑—ã–≤–∞–µ—Ç —Ä–∞—Å—à–∏—Ä–µ–Ω–Ω—É—é —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É –±–æ—Ç–∞
    """
    # –ü—Ä–æ–≤–µ—Ä–∫–∞ –ø—Ä–∞–≤ –∞–¥–º–∏–Ω–∏—Å—Ç—Ä–∞—Ç–æ—Ä–∞
    if callback.from_user.id != config.ADMIN_ID:
        await callback.answer("‚ùå –î–æ—Å—Ç—É–ø –∑–∞–ø—Ä–µ—â–µ–Ω", show_alert=True)
        return
    
    # –ü–æ–ª—É—á–∞–µ–º —è–∑—ã–∫ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
    user_lang = db.get_user_language(callback.from_user.id)
    if not user_lang:
        user_lang = 'ru'
    
    # –ü–æ–ª—É—á–∞–µ–º —Å—Ç–∞—Ç–∏—Å—Ç–∏–∫—É
    total_users = db.get_users_count()
    today_users = db.get_users_count_today()
    
    stats_text = get_text(
        user_lang,
        'statistics_text',
        total=total_users,
        today=today_users
    )
    
    await callback.message.edit_text(
        text=stats_text,
        reply_markup=get_admin_keyboard(user_lang),
        parse_mode="HTML"
    )
    
    await callback.answer()


